    public void preencherLista() throws SQLException{
        
             
        EscolherFilme ef = new EscolherFilme();
        Filme f =ef.filmeEscolhido();
        
        txtFilme.setText(f.getNome());
        
        SessaoDAO dao = new SessaoDAO();
        
        ArrayList<Sessao> sessoes = dao.procurarPorFilme(f.getId());
        
        for(Sessao s: sessoes){
            this.listaSessao.addItem(s.getHorario());
        }
    }
    
