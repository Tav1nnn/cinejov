    private void listaSessaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listaSessaoActionPerformed
        // TODO add your handling code here: 
        SessaoDAO dao = new SessaoDAO();
        
        String horario = (String) listaSessao.getSelectedItem();   
        
        try {
            SalaDAO sdao = new SalaDAO();
             
            EscolherFilme ef = new EscolherFilme();
            
            Filme f = ef.filmeEscolhido();
            
            Sessao s = dao.procurarUmaSessao(f.getId(), horario);
            
            Sala sl = sdao.procurarPorID(s.getIdSala());
               
            txtSala.setText(sl.getNome());
            txtFilme.setText(f.getNome());
            
        } catch (SQLException ex) {
            Logger.getLogger(EscolherSessao.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_listaSessaoActionPerformed

