    private void btnFinalizarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarCompraActionPerformed
        // TODO add your handling code here:
        ArrayList<JCheckBox> box = this.retornarArray();
        
        for(JCheckBox checkbox: box) {
            if(checkbox.isSelected() && checkbox.isEnabled()) {
                IngressoDAO dao = new IngressoDAO();
                Ingresso i = new Ingresso();
                
                LoginCliente lc = new LoginCliente();
                Cliente c = lc.clienteLogado();
                
                EscolherSessao es = null;
                
                int index = box.indexOf(checkbox);
                try {
                    es = new EscolherSessao();
                } catch (SQLException ex) {
                    Logger.getLogger(CompraIngresso.class.getName()).log(Level.SEVERE, null, ex);
                }
                Sessao s = es.retornarSessao();
                i.setIdCliente(c.getId());
                i.setIdSessao(s.getId());
                i.setLugar(index);
                try {
                   dao.inserirIngresso(i);
                } catch (SQLException ex) {
                    Logger.getLogger(CompraIngresso.class.getName()).log(Level.SEVERE, null, ex);
                }
            }   
        }
        JOptionPane.showMessageDialog(this, "Compra realizada com sucesso!");
        this.dispose();
        try {
            EscolherFilme ef = new EscolherFilme();
            ef.setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(CompraIngresso.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnFinalizarCompraActionPerformed

