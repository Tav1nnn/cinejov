    public void recuperarIngressosVendidos() throws SQLException{
        IngressoDAO dao = new IngressoDAO();
        
        EscolherSessao es = new EscolherSessao();
        Sessao s = es.retornarSessao();
        
        ArrayList<Ingresso> ingressos = dao.procurarPorSessao(s.getId());
        
        
        ArrayList<JCheckBox> box = this.retornarArray();
               
        
        if(ingressos.size()>0){
        
            for(Ingresso i: ingressos){
                int lugar = i.getLugar();
                
               JCheckBox checkbox = box.get(lugar);
               checkbox.setSelected(true);
               checkbox.setEnabled(false);
               checkbox.setBackground(Color.RED);           
            }
        }
        
    }
    
