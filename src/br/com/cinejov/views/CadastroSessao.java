    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        String nomeSala = (String) this.listaSala.getSelectedItem();
        String nomeFilme = (String) this.listaFilme.getSelectedItem();
        
        if(txtHora.getText().trim().length() != 5){
            JOptionPane.showMessageDialog(this, "Horário deve ter 5 caracteres. Ex: 21:30");
            txtHora.setText("");
            return;
        }
        String horario = txtHora.getText();
        
        SalaDAO salaDao = new SalaDAO();
        FilmeDAO filmeDao = new FilmeDAO();
        
        Sala s = null ;
        Filme f = null ;
        
        try {
             s = salaDao.procurarNome(nomeSala);
        } catch (SQLException ex) {
            Logger.getLogger(CadastroSessao.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            f = filmeDao.procurarNome(nomeFilme);
        } catch (SQLException ex) {
            Logger.getLogger(CadastroSessao.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        SessaoDAO dao = new SessaoDAO();
        
        Sessao sessao = new Sessao(s.getId(), f.getId(), horario);
        try {
            dao.inserir(sessao);
            JOptionPane.showMessageDialog(this, "Sessão cadastrada");
            txtHora.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(CadastroSessao.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

