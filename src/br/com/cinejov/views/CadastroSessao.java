    public void preencherListaSala() throws SQLException{
        SalaDAO dao = new SalaDAO();
        
        ArrayList<Sala> salas = new ArrayList();
        
        salas = dao.selecionarTodos();
        
        for(Sala s: salas){
            this.listaSala.addItem(s.getNome());
            
        }
    }
    
    public void preencherListaFilme() throws SQLException{
        FilmeDAO dao = new FilmeDAO();
        
        ArrayList<Filme> filmes = new ArrayList();
        
        filmes = dao.selecionarTodos();
        
        for(Filme f: filmes){
            this.listaFilme.addItem(f.getNome());
            
        }
        
    }
        
    
